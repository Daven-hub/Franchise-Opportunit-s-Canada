                Au Québec, le Ministère des Municipalités et de l’Habitation incarne une vision globale et intégrée du fonctionnement et du développement des Municipalités, des MRC et des ARK. Par contre, des organismes comme la Fédération Québécoise des Municipalités et l’Union Québécoise des Municipalités participent respectivement à une cohésion inter-municipale ainsi qu’à une valorisation des potentiels économiques et culturels des municipalités québécoises à travers notamment le Salon PMEs Municipales (FQM), la Semaine de la Municipalité et le Collaboratoire Uni-cité (UQM).
                De l’autre côté, en Afrique francophone, les Communes, comme on les appelle, commencent seulement tout doucement à s’émanciper des tutelles politiques du pouvoir central. La mise en place des Ministères de la Décentralisation et l’Aménagement du territoire au Sénégal, en Côte d’Ivoire, au Burkina-Faso, en République Démocratique du Congo, au Gabon et au Cameroun, participe à cette nouvelle approche de gouvernance territoriale axée sur le développement et l’autonomie des Communes. Suivant cette approche, la majorité de ces pays d’Afrique francophone s’est dotée d’une organisation supra-communale : l’Association des Maires du Sénégal (AMS), l’Union des Villes et Communes de Côte d’Ivoire (UVICOCI), l’Association des Municipalités du Burkina Faso (AMBF), l’Association nationale des villes et communes de la RDC (ANVC/RDC), l’Association des Maires du Gabon (AMG) et les Communes et Villes Unies du Cameroun (CVUC). 

