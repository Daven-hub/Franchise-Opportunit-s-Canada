                    <p>Dans le secteur des équipements médicaux, Business France mentionne dans une étude publiée en septembre 2020 que le marché des équipements médicaux en Afrique subsaharienne est estimé à 5 Mds USD. Si les besoins sont colossaux en solutions innovantes de traitement des déchets médicaux, de décontamination, de diagnostic, les opérateurs du secteur médical en Afrique se tournent de plus en plus vers des solutions de technologies connectées. La pandémie du Covid19 a d’ailleurs renforcé l’utilité et l’usage de ces applications. Ce qui a rendu favorable l’émergence des centres de télémédecine, qui nécessitent, au demeurant, une dotation high-tech importante. Quoique d’après l’OMS, plus de 40% des équipements médicaux ne sont pas opérationnels en Afrique, principalement à cause d’une pénurie de compétences, de problèmes liés aux pièces de rechange qui conduisent à un coût de possession élevé. 
                        FOC Inc a une vision bilatérale des affaires et mise sur une qualité exceptionnelle d’opportunités d’affaires pour mettre en réseau plusieurs interlocuteurs privé-privé, privé-public et public-public, dans une approche de commerce international, d’échange de services, de développement économique, de prestation de services et de consultance internationale. 
                        Notre volet OA va prioritairement se focaliser sur les opportunités d’achat, de location et/ou de leasing d’engins de BTP, agricoles, miniers (avec service d’accompagnement pour la maintenance et le suivi d’entretien ou de remplacement de pièces) ainsi que sur la fourniture d’équipements médicaux et l’assistance technologique et opérationnelle. 
                        Par ce volet clé, FOC Inc entend créer une plateforme de partenaires évoluant dans la fabrication, l’import/export de ces deux catégories de gros matériels. Cette plateforme permettra des mises en relation directe avec des personnes d’intérêt, des sociétés, des collectivités et des Etats basés en Afrique Centrale et de l’Ouest prioritairement. Le but étant de faciliter l’accès aux équipements de qualité, garantie par un savoir-faire reconnu et une adaptabilité tropicale.
                        </p>
